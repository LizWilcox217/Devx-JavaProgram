package com.java.class08;

public class CheckCharIsVowel {
    public static void main(String[] args) {

        char c = 'a';


        if(c =='a'||c=='e'||c=='i'||c=='o'||c=='u'){

            System.out.println("Your letter is a vowel");
        }else{
            System.out.println("Not vowel");

        }
    }
}
