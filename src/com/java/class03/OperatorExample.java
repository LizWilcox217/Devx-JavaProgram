package com.java.class03;

public class OperatorExample {

    public static void main(String[] args) {
        System.out.println("Test");

        int a = 10, b = 20;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(b/a);

        System.out.println(b % a);
        System.out.println(10 % 3);



    }
}
